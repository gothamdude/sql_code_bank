This recipe demonstrates how to use the SET TRANSACTION ISOLATION LEVEL command to set the
default transaction locking behavior for Transact-SQL statements used in a connection. You can
have only one isolation level set at a time, and the isolation level does not change unless explicitly
set. SET TRANSACTION ISOLATION LEVEL allows you to change the locking behavior for a specific
database connection. The syntax for this command is as follows:

SET TRANSACTION ISOLATION LEVEL
{ READ UNCOMMITTED
| READ COMMITTED
| REPEATABLE READ
| SNAPSHOT
| SERIALIZABLE
}

In this first example, SERIALIZABLE isolation is used to query the contents of a table. In the first
query editor window, the following code is executed:
