USE AdventureWorks
GO
SET TRANSACTION ISOLATION LEVEL SERIALIZABLE 
GO

BEGIN TRAN

SELECT AddressTypeID, Name
FROM Person.AddressType
WHERE AddressTypeID BETWEEN 1 AND 6

-- In a second query editor, the following query is executed to view the kinds of locks generated by
-- the SERIALIZABLE isolation level:

SELECT resource_associated_entity_id, resource_type,
request_mode, request_session_id
FROM sys.dm_tran_locks

--Back in the first query editor window, execute the following code to end the transaction and
--remove the locks:

COMMIT TRAN

--In contrast, the same query is executed again in the first query editor window, this time using
--the READ UNCOMMITTED isolation level to read the range of rows:

SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED
GO

BEGIN TRAN
SELECT AddressTypeID, Name
FROM Person.AddressType
WHERE AddressTypeID BETWEEN 1 AND 6

-- In a second query editor, the following query is executed to view the kinds of locks generated
-- by the READ UNCOMMITTED isolation level:

SELECT resource_associated_entity_id, resource_type,
request_mode, request_session_id
FROM sys.dm_tran_locks


--Unlike SERIALIZABLE, the READ UNCOMMITTED isolation level creates no additional locks on the
--keys of the Person.AddressType table.
--Returning back to the first query editor with the READ UNCOMMITTED query, the transaction is
--ended for cleanup purposes:

COMMIT TRAN

